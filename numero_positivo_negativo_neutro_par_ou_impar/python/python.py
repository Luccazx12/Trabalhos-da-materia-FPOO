#Entrada
n = int(input('Digite um valor: '))

#Processamento e Saídas
if n > 0:
    print('Seu número é positivo.')

elif n < 0:
    print('Seu número é negativo.')

else:
    print('Seu número é neutro.')

if n%2 ==0:
    print('Seu número é par')

else:
    print('Seu número é impar')